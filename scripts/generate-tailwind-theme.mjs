import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const tokenDir = path.join(process.cwd(), "design-tokens");
const appThemeOutputPath = path.join(process.cwd(), "src/app/theme.css");
const storybookTokenOutputPath = path.join(process.cwd(), "src/tailwind-theme/tailwind-theme.tokens.css");
const modes = ["light", "dark"];
const namespace = "ad";
const storybookTokenGroups = [
  {
    title: "Colors",
    presenter: "Color",
    filter: (name, token) => token.$type === "color" && (name.startsWith("color/") || name.startsWith("chart/")),
  },
  {
    title: "Radius",
    presenter: "BorderRadius",
    filter: (name) => name.startsWith("radius/"),
  },
  {
    title: "Spacing",
    presenter: "Spacing",
    filter: (name) => name.startsWith("space/"),
  },
];

const modeTokens = Object.fromEntries(
  await Promise.all(
    modes.map(async (mode) => [
      mode,
      JSON.parse(await readFile(path.join(tokenDir, `${mode}.tokens.json`), "utf8"))
    ])
  )
);

function cssVarName(tokenName) {
  return `--${namespace}-${tokenName.replaceAll("/", "-")}`;
}

function utilityName(tokenName) {
  return tokenName.replace(/^color\//, "").replace(/^chart\//, "chart-").replaceAll("/", "-");
}

function flattenTokens(group, prefix = []) {
  const entries = [];

  for (const [key, value] of Object.entries(group)) {
    if (value && typeof value === "object" && "$type" in value && "$value" in value) {
      entries.push([prefix.concat(key).join("/"), value]);
    } else if (value && typeof value === "object") {
      entries.push(...flattenTokens(value, prefix.concat(key)));
    }
  }

  return entries;
}

function tokenValue(token) {
  const value = token.$value;

  if (token.$type === "color") {
    return value.hex;
  }

  if (token.$type === "dimension") {
    return `${value.value}${value.unit}`;
  }

  return value;
}

const tokensByMode = Object.fromEntries(
  Object.entries(modeTokens).map(([mode, tokens]) => [mode, Object.fromEntries(flattenTokens(tokens[namespace]))])
);

const tokenNames = Object.keys(tokensByMode.light);

function modeBlock(mode) {
  const lines = tokenNames.map((name) => `  ${cssVarName(name)}: ${tokenValue(tokensByMode[mode][name])};`);
  const block = `  ${lines.join("\n  ")}`;

  if (mode === "light") {
    return `:root {\n${lines.join("\n")}\n}`;
  }

  return `@media (prefers-color-scheme: dark) {\n  :root {\n${block}\n  }\n}`;
}

function themeMappings() {
  const lines = [];
  for (const name of tokenNames) {
    const token = tokensByMode.light[name];
    if (token.$type === "color") {
      lines.push(`  --color-${namespace}-${utilityName(name)}: var(${cssVarName(name)});`);
    }
    if (token.$type === "dimension" && name.startsWith("radius/")) {
      lines.push(`  --radius-${namespace}-${utilityName(name).replace("radius-", "")}: var(${cssVarName(name)});`);
    }
  }
  return `@theme inline {\n${lines.join("\n")}\n}`;
}

function tailwindReference(tokenName, token) {
  if (tokenName.startsWith("color/")) {
    return `bg-ad-${utilityName(tokenName)}, text-ad-${utilityName(tokenName)}, border-ad-${utilityName(tokenName)}`;
  }

  if (tokenName.startsWith("chart/")) {
    return `text-ad-${utilityName(tokenName)}`;
  }

  if (token.$type === "dimension" && tokenName.startsWith("radius/")) {
    return `rounded-ad-${utilityName(tokenName).replace("radius-", "")}`;
  }

  return "";
}

function storybookTokenGroup({ title, presenter, filter }) {
  const lines = tokenNames
    .filter((name) => filter(name, tokensByMode.light[name]))
    .map((name) => {
      const token = tokensByMode.light[name];
      const references = [`Figma: ${namespace}/${name}`];
      const tailwind = tailwindReference(name, token);
      if (tailwind) {
        references.push(`Tailwind: ${tailwind}`);
      }

      return `  ${cssVarName(name)}: ${tokenValue(token)}; /* ${references.join(". ")}. */`;
    });

  return `/**\n * @tokens ${title}\n * @presenter ${presenter}\n */\n:root {\n${lines.join("\n")}\n}`;
}

const appThemeCss = [
  "/* Generated by scripts/generate-tailwind-theme.mjs. Do not edit by hand. */",
  ...modes.map(modeBlock),
  themeMappings(),
  ""
].join("\n\n");

const storybookTokenCss = [
  "/* Generated by scripts/generate-tailwind-theme.mjs. Do not edit by hand. */",
  ...storybookTokenGroups.map(storybookTokenGroup),
  ""
].join("\n\n");

await Promise.all([
  mkdir(path.dirname(appThemeOutputPath), { recursive: true }),
  mkdir(path.dirname(storybookTokenOutputPath), { recursive: true }),
]);
await Promise.all([
  writeFile(appThemeOutputPath, appThemeCss),
  writeFile(storybookTokenOutputPath, storybookTokenCss),
]);
